public class Vijand {
    private String naam;
    private String omschrijving;
    private int levenspunten;
    private int aanvalspunten;

    public Vijand(String naam, String omschrijving, int levenspunten, int aanvalspunten) {
        this.naam = naam;
        this.omschrijving = omschrijving;
        this.levenspunten = levenspunten;
        this.aanvalspunten = aanvalspunten;
    }
}
